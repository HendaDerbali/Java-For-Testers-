package myPackage;

public class DataTypes {

	public static void main(String[] args) {

		// Permeative
		int a = 10;
		double d = 10.555;
		char c = 'A';
		boolean b = false;
		
		// Derived 
		String s = "Welcome";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
		System.out.println(s);

	}

}
